#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	dst_len;
	size_t	src_len;
	size_t	i;

	dst_len = ft_strlen(dst);
	src_len = ft_strlen(src);
	if (size <= dst_len)
		return (size + src_len);
	i = dst_len;
	while (*src && i < size - 1)
	{
		dst[i++] = *src++;
	}
	dst[i] = '\0';
	return (dst_len + src_len);
}
